first_number = int(input("Enter the first number: "))    #Take the input from user and convert it to int. We can take float as input as well
second_number = int(input("Enter the second number: "))  #Take the second input from user similar to first one.

Addition = first_number + second_number  #Addition of two numbers
Subtraction = first_number - second_number #Substraction of two numbers
Multiplication = first_number * second_number #Multiplication of two numbers
Division = first_number / second_number #Division of two numbers

#Below is the display of all operations
print("Addition: ", Addition)
print("Subtraction: ", Subtraction)
print("Multiplication: ", Multiplication)
print("Division: ",Division)