import math as m   #importing of modules of math

num=int(input("Enter a number: "))    #Taking input from user.

print(f"Square root: {m.sqrt(num)}")  #Finding square root of number.

print(f"Logarithm: {m.log(num)}")     #Finding logarithm of number.

print(f"Sine: {m.sin(num)}")          #Finding Sine of number.